import type { City } from '@/types'
import type { ShoppingGuideBundle, ShoppingKind, ShoppingSpot } from './cityShoppingGuide'

function mqs(q: string): string {
  return `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(q)}`
}

type Ctx = Pick<City, 'name' | 'slug' | 'region'>

type FullShopping = Record<ShoppingKind, ShoppingGuideBundle>

const cache = new Map<string, FullShopping>()

function spot(
  slug: string,
  sid: string,
  name: string,
  area: string,
  typeTag: string,
  priceBand: string,
  hours: string,
  addressHint: string,
  mapQuery: string,
  bestBuys: string[],
  tips: string[],
): ShoppingSpot {
  return {
    id: `${slug}-sh-${sid}`,
    name,
    area,
    typeTag,
    priceBand,
    hours,
    addressHint,
    mapUrl: mqs(mapQuery),
    reviewsUrl: mqs(`${mapQuery} shopping reviews`),
    bestBuys,
    tips,
  }
}

